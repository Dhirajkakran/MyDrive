package com.example.bean;

public class VehicleCategory {
	private String vehichleID;
	private String name;
	private String ratePerKM;
	private String baseFare;
	private String waitPerMinute;
	private String description;
	private String currency;
	public String getVehichleID() {
		return vehichleID;
	}
	public void setVehichleID(String vehichleID) {
		this.vehichleID = vehichleID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRatePerKM() {
		return ratePerKM;
	}
	public void setRatePerKM(String ratePerKM) {
		this.ratePerKM = ratePerKM;
	}
	public String getBaseFare() {
		return baseFare;
	}
	public void setBaseFare(String baseFare) {
		this.baseFare = baseFare;
	}
	public String getWaitPerMinute() {
		return waitPerMinute;
	}
	public void setWaitPerMinute(String waitPerMinute) {
		this.waitPerMinute = waitPerMinute;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getCurrency() {
		return currency;
	}
	public void setCurrency(String currency) {
		this.currency = currency;
	}
	
 
 

}
